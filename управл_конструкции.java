public class управл_конструкции {
    public static void main(String[] args) {
        int a = 1;
        int b = 2;
        int c;
        if (a > b) {
            c = a;
        } else {
            c = b;
        }
        System.out.println (c) ;

        int i = 1;
        int j = 2;
        int min = i < j? i : j;
        System.out.println (min);
    }
}
